package com.example.mygallery;

class GalleryItem {
    private String caption;
    private String id;
    private String url;

    String getCaption() {
        return caption;
    }

    void setCaption(String caption) {
        this.caption = caption;
    }

    String getId() {
        return id;
    }

    void setId(String id) {
        this.id = id;
    }

    String getUrl() {
        return url;
    }

    void setUrl(String url) {
        this.url = url;
    }

}
